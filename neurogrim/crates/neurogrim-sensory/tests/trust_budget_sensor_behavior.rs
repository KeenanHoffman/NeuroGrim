// ── 8. Per-hat composition: violation_potential ─────────────────────

#[tokio::test]
async fn per_hat_composition_violation_potential() {
    let tmp = make_brain_root();

    // Declare a shell-out token in trust-budget.toml. We pick `cargo`
    // — a common, real-world shell-out command name.
    let toml_body = r#"schema_version = "1"

[[declared_shell_outs]]
command_name = "cargo"
script_paths = ["scripts/build.sh"]
"#;
    write_trust_budget(tmp.path(), toml_body);

    // Add a script that invokes the declared command (so the actual
    // shell-out surface contains it, no overdeclared finding).
    let scripts_dir = tmp.path().join("scripts");
    std::fs::create_dir_all(&scripts_dir).expect("create scripts dir");
    std::fs::write(
        scripts_dir.join("build.sh"),
        "#!/bin/bash\nset -euo pipefail\ncargo build --release\n",
    )
    .expect("write build.sh");

    // Author a hat declaring `forbidden_tools: [cargo]`. Note: the
    // hat-contract schema's closed set doesn't include `cargo` as a
    // ToolName — but that schema is enforced by capability_hygiene's
    // hat-contract validator, NOT by trust_budget. Trust-budget reads
    // forbidden_tools as raw strings + cross-references against
    // declared_shell_outs by literal token match. To keep the test
    // self-contained we use a token that matches the hat-contract
    // closed-set vocabulary AND will appear in declared_shell_outs.
    // `Bash` is a hat-contract ToolName; we declare it as a shell-out
    // command_name (the operator chose to encode a Bash-script
    // invocation surface that way).
    let hats_dir = tmp.path().join(".claude").join("skills").join("hats");
    std::fs::create_dir_all(&hats_dir).expect("create hats dir");

    // Use the `cargo` token in the hat — same name as the script
    // invocation. The hat-contract schema would reject `cargo` as a
    // ToolName (closed-set), but trust-budget cross-references by
    // literal string and emits a violation_potential when the tokens
    // match. This is exactly the documented Q8 / E4-5 semantics.
    let hat_body = "---\n\
        name: paranoid-builder\n\
        description: Test fixture for trust-budget per-hat composition — declares cargo as forbidden.\n\
        forbidden_tools:\n  - cargo\n\
        ---\n\
        \n\
        Hat fixture for per-hat composition test.\n";
    std::fs::write(hats_dir.join("paranoid-builder.md"), hat_body)
        .expect("write hat");

    let result = analyze_trust_budget(tmp.path().to_str().unwrap()).await;

    let violation_findings = findings_by_prefix(
        &result,
        "trust_budget:per_hat:violation_potential:",
    );
    assert_eq!(
        violation_findings.len(),
        1,
        "expected exactly 1 violation_potential finding; got: {:?}",
        trust_budget_findings(&result)
    );
    let f = violation_findings[0];
    assert_eq!(
        f["name"],
        "trust_budget:per_hat:violation_potential:paranoid-builder:cargo",
        "violation_potential finding name must follow the documented `<hat>:<token>` shape; got: {:?}",
        f
    );
    assert_eq!(f["status"], "neutral");
    assert_eq!(f["points"], 0);
}

// ── 9. Live NeuroGrim smoke ──────────────────────────────────────────

/// Locate the live NeuroGrim Brain root from the crate's manifest dir.
/// Crate sits at `<root>/NeuroGrim/neurogrim/crates/neurogrim-sensory/`;
/// three `..` hops up lands at `<root>/NeuroGrim/`. Returns `None`
/// when the layout doesn't match (standalone checkout) — same skip-
/// when-absent convention as `hat_contract_sensor_behavior.rs`.
fn locate_neurogrim_brain_root() -> Option<PathBuf> {
    let manifest_dir = PathBuf::from(env!("CARGO_MANIFEST_DIR"));
    // Three `..` hops up from `<brain>/neurogrim/crates/neurogrim-sensory`
    // lands at the Brain root `<brain>/`. Canonicalize so `.is_dir()`
    // checks resolve real path components on Windows. Mirrors
    // `hat_contract_sensor_behavior.rs::locate_neurogrim_brain_root`.
    let brain_root = manifest_dir.join("../../..").canonicalize().ok()?;
    if brain_root.join(".claude").is_dir() {
        Some(brain_root)
    } else {
        None
    }
}

/// Smoke-test against the live NeuroGrim Brain root. WITHOUT a
/// `trust-budget.toml` in place yet (Component 4 hasn't shipped),
/// expect ONE `trust_budget:declaration:missing` finding plus drift
/// findings (the actual surface vs empty declared).
#[tokio::test]
async fn live_neurogrim_smoke() {
    let Some(brain_root) = locate_neurogrim_brain_root() else {
        eprintln!("skip: live NeuroGrim brain root not reachable");
        return;
    };

    // If a trust-budget.toml has been authored (e.g., Component 4 has
    // shipped by the time this test runs), skip the missing-finding
    // assertion — instead we just assert the envelope is well-formed
    // and document the count of drift findings.
    let toml_present = brain_root.join("trust-budget.toml").is_file();

    let result = analyze_trust_budget(brain_root.to_str().unwrap()).await;

    // Envelope is well-formed.
    assert_eq!(result["meta"]["updated_by"], "trust-budget");
    assert!(result["score"].is_number());

    let all = trust_budget_findings(&result);
    eprintln!(
        "live_neurogrim_smoke: trust_budget.toml present = {toml_present}, total findings = {}",
        all.len()
    );
    // Print the first 3 finding kinds (`name` field) for the dogfood
    // baseline record. Helps the C7 integration step calibrate whether
    // the count drifted unexpectedly between runs.
    for (i, f) in all.iter().take(3).enumerate() {
        eprintln!(
            "  finding[{i}] name = {}",
            f["name"].as_str().unwrap_or("<missing>")
        );
    }
    let undeclared = findings_by_prefix(&result, "trust_budget:undeclared:");
    let overdeclared = findings_by_prefix(&result, "trust_budget:overdeclared:");
    let invalid = findings_by_prefix(&result, "trust_budget:declaration:invalid:");
    let missing = findings_by_prefix(&result, "trust_budget:declaration:missing");
    let malformed = findings_by_prefix(&result, "trust_budget:declaration:malformed");
    let per_hat = findings_by_prefix(&result, "trust_budget:per_hat:");
    eprintln!(
        "  baseline: undeclared={}, overdeclared={}, invalid={}, missing={}, \
         malformed={}, per_hat={}",
        undeclared.len(),
        overdeclared.len(),
        invalid.len(),
        missing.len(),
        malformed.len(),
        per_hat.len(),
    );

    if !toml_present {
        // No trust-budget.toml yet. Exactly one `:declaration:missing`
        // finding; no malformed; no invalid.
        assert_eq!(
            missing.len(),
            1,
            "without trust-budget.toml, expected exactly 1 declaration:missing finding"
        );
        assert!(
            malformed.is_empty(),
            "no malformed without a file present; got: {:?}",
            malformed
        );
        assert!(
            invalid.is_empty(),
            "no invalid without a parseable file; got: {:?}",
            invalid
        );

        // ── Smoke-shape assertions (post-bug-fix calibration) ──
        //
        // After C3 was patched (2026-04-27) to fix three scanner bugs
        // (function-name false positives, zero-crate detection, narrow
        // service walk), the smoke-output should look qualitatively
        // different from the original 59-finding noise. The asserts
        // below pin the expected shape so future regressions surface
        // immediately.

        // Total finding cap. The original buggy run produced 59. The
        // patched run produces ~66 with the broader (but correct)
        // service walk. We allow up to 100 to give headroom for new
        // direct deps without needing a re-pin every time the
        // workspace grows; if we ever drift over 100 it's a calibration
        // smell worth investigating.
        assert!(
            all.len() <= 100,
            "live smoke total findings should be ≤ 100 (post-fix calibration ceiling); got {}",
            all.len()
        );

        // Shell-out vocabulary check: at least one of the well-known
        // external commands MUST appear (this was zero in the buggy
        // output for `git`/`bash`/`python`; `curl`/`docker` were
        // already present but accompanied by 40+ false-positives).
        let shell_out_kinds: Vec<String> = all
            .iter()
            .filter_map(|f| f["name"].as_str())
            .filter(|n| n.starts_with("trust_budget:undeclared:shell_out:"))
            .map(|n| {
                n.trim_start_matches("trust_budget:undeclared:shell_out:")
                    .to_string()
            })
            .collect();
        let has_real_external = shell_out_kinds.iter().any(|k| {
            matches!(
                k.as_str(),
                "curl" | "docker" | "git" | "cargo" | "bash" | "python"
            )
        });
        assert!(
            has_real_external,
            "expected at least one real external command (curl/docker/git/cargo/bash/python) in shell-out findings; got: {:?}",
            shell_out_kinds
        );

        // Bug-1 forbidden tokens — these were in the original buggy
        // output and MUST NOT reappear. Single tokens:
        let bug1_forbidden_tokens = [
            "<network>",
            "Inspect",
            "See",
            "red",
            "green",
            "blue",
            "yellow",
            "done",
            "ok",
            "break",
            "info",
            "import",
            "main",
            "log",
            "fail",
        ];
        for tok in bug1_forbidden_tokens {
            assert!(
                !shell_out_kinds.iter().any(|k| k == tok),
                "shell_out token `{tok}` is a known false-positive (Bug 1); got shell-outs: {:?}",
                shell_out_kinds
            );
        }
        // Prefix-class forbidden — bash function names from prepublish-check.sh.
        for k in &shell_out_kinds {
            assert!(
                !k.starts_with("check_"),
                "shell_out token `{k}` starts with `check_` — that's a bash function name from prepublish-check.sh, not a real external command (Bug 1)"
            );
            assert!(
                !k.starts_with("neurogrim-"),
                "shell_out token `{k}` starts with `neurogrim-` — that's a workspace crate name, not a real external command (Bug 1)"
            );
        }

        // Bug-2 — crates set MUST be non-empty. Pre-fix it was zero.
        let breakdown = &result["trust_budget_breakdown"];
        let actual_crates = breakdown["actual"]["crates"]
            .as_array()
            .map(|a| a.len())
            .unwrap_or(0);
        assert!(
            actual_crates >= 5,
            "expected at least 5 direct deps in actual.crates (Bug 2 was zero); got {}",
            actual_crates
        );

        // Bug-3 — services set MUST be non-empty.
        let actual_services = breakdown["actual"]["services"]
            .as_array()
            .map(|a| a.len())
            .unwrap_or(0);
        assert!(
            actual_services >= 1,
            "expected at least 1 FQDN in actual.services (Bug 3 was zero); got {}",
            actual_services
        );
    }
}

