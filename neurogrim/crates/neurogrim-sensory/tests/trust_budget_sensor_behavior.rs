//! E-B2-4 C3 — `analyze_trust_budget` behavioral tests.
//!
//! Mirrors the E-B2-3 `hat_contract_sensor_behavior.rs` shape:
//! synthetic-`TempDir` fixtures populated programmatically, then
//! observed via the public `analyze_trust_budget` entry point's CMDB
//! envelope output.
//!
//! Coverage (10 cases including recursion-guard pin):
//!
//!  1. **`missing_trust_budget_emits_declaration_finding`** — synthetic
//!     TempDir with no `trust-budget.toml`; one
//!     `trust_budget:declaration:missing` finding emitted; envelope
//!     well-formed.
//!  2. **`well_formed_trust_budget_no_drift_findings`** — synthetic
//!     root with TOML declaring exactly the surface the sensor
//!     computes; ZERO drift findings.
//!  3. **`undeclared_crate_emits_finding`** — `Cargo.lock` has `serde`
//!     but TOML missing it; one `trust_budget:undeclared:crate:serde`
//!     finding.
//!  4. **`overdeclared_crate_emits_finding`** — TOML declares
//!     `nonexistent_crate`; `Cargo.lock` doesn't have it; one
//!     `trust_budget:overdeclared:crate:nonexistent_crate` finding.
//!  5. **`seeded_crate_suppresses_undeclared_finding`** — `Cargo.lock`
//!     has crate `bootstrap_dep`; TOML has `bootstrap_dep` with
//!     `seeded: true`; ZERO findings about `bootstrap_dep`.
//!  6. **`malformed_trust_budget_aborts_gracefully`** — `trust-budget.toml`
//!     with invalid TOML; one `trust_budget:declaration:malformed`
//!     finding; no panic; envelope returns.
//!  7. **`invalid_trust_budget_emits_per_error_findings`** — TOML
//!     parses but fails schema validation (wrong `trust_posture`);
//!     one or more `trust_budget:declaration:invalid:*` findings.
//!  8. **`per_hat_composition_violation_potential`** — synthetic root
//!     with a hat declaring `forbidden_tools: [<token>]` AND TOML
//!     declaring the same token in `declared_shell_outs`; verify the
//!     `trust_budget:per_hat:violation_potential:*` finding is
//!     emitted with the documented shape. Static "could-be"
//!     semantics; runtime is BACKLOG B-23.
//!  9. **`live_neurogrim_smoke`** — call `analyze_trust_budget`
//!     against the actual `D:/Brains/NeuroGrim` repo without a
//!     `trust-budget.toml` in place yet (Component 4 hasn't shipped);
//!     emits ONE `trust_budget:declaration:missing` finding plus
//!     drift findings. Documents the count as a baseline.
//! 10. **`recursion_guard_no_command_in_validator_span`** (Q7) — pin
//!     the Q7 hard rule: `trust_budget.rs` source contains no
//!     `Command::new` / `process::Command` / `Stdio` / `std::process`
//!     in its function spans, and `Cargo.toml` doesn't declare
//!     `walkdir` / `subprocess` / `duct` / `which` / `shell-words`.

use serde_json::Value;
use std::path::{Path, PathBuf};
use tempfile::TempDir;

use neurogrim_sensory::trust_budget::analyze_trust_budget;

// ── Fixture helpers ─────────────────────────────────────────────────

/// Make a tempdir that looks like a Brain root.
fn make_brain_root() -> TempDir {
    tempfile::tempdir().expect("tempdir")
}

/// Write a `trust-budget.toml` at the root.
fn write_trust_budget(root: &Path, body: &str) {
    std::fs::write(root.join("trust-budget.toml"), body).expect("write trust-budget.toml");
}

/// Write a minimal `Cargo.lock` with the given crate names. Versions
/// are dummy `1.0.0`; sources are `crates.io`.
fn write_cargo_lock(root: &Path, crates: &[&str]) {
    let mut buf = String::from("# This file is automatically @generated by Cargo.\nversion = 3\n\n");
    for (i, name) in crates.iter().enumerate() {
        // The cargo-lock crate validates checksum syntax (32-byte hex).
        // Pad with a unique trailing index so duplicates differ.
        let checksum: String = format!(
            "{:0width$x}",
            i + 1,
            width = 64
        );
        buf.push_str(&format!(
            "[[package]]\nname = \"{name}\"\nversion = \"1.0.0\"\nsource = \"registry+https://github.com/rust-lang/crates.io-index\"\nchecksum = \"{checksum}\"\n\n"
        ));
    }
    std::fs::write(root.join("Cargo.lock"), buf).expect("write Cargo.lock");
}

/// Filter findings by name prefix.
fn findings_by_prefix<'a>(result: &'a Value, prefix: &str) -> Vec<&'a Value> {
    result["findings"]
        .as_array()
        .map(|arr| {
            arr.iter()
                .filter(|f| {
                    f["name"]
                        .as_str()
                        .map(|s| s.starts_with(prefix))
                        .unwrap_or(false)
                })
                .collect()
        })
        .unwrap_or_default()
}

/// All `trust_budget:*` findings.
fn trust_budget_findings(result: &Value) -> Vec<&Value> {
    findings_by_prefix(result, "trust_budget:")
}

/// Locate the `trust_budget.rs` source file from the crate's manifest dir.
fn locate_trust_budget_source() -> PathBuf {
    PathBuf::from(env!("CARGO_MANIFEST_DIR"))
        .join("src")
        .join("trust_budget.rs")
}

// ── 1. Missing trust-budget.toml → declaration finding ──────────────

#[tokio::test]
async fn missing_trust_budget_emits_declaration_finding() {
    let tmp = make_brain_root();

    let result = analyze_trust_budget(tmp.path().to_str().unwrap()).await;

    let missing = findings_by_prefix(&result, "trust_budget:declaration:missing");
    assert_eq!(
        missing.len(),
        1,
        "expected exactly 1 declaration:missing finding; got: {:?}",
        trust_budget_findings(&result)
    );
    assert_eq!(missing[0]["status"], "neutral");
    assert_eq!(missing[0]["points"], 0);

    // Envelope still well-formed.
    assert!(result["score"].is_number(), "score must be a number");
    assert_eq!(result["meta"]["updated_by"], "trust-budget");

    // Breakdown JSON must exist and report declaration_present = false.
    let breakdown = &result["trust_budget_breakdown"];
    assert!(breakdown.is_object(), "breakdown must be present");
    assert_eq!(breakdown["declaration_present"], false);
    assert_eq!(breakdown["declared"]["crate_count"], 0);
}

// ── 2. Well-formed declaration matching surface → no drift ──────────

#[tokio::test]
async fn well_formed_trust_budget_no_drift_findings() {
    let tmp = make_brain_root();
    // Synthetic Cargo.lock with one crate.
    write_cargo_lock(tmp.path(), &["serde"]);
    // TOML declaring exactly that crate.
    let toml_body = r#"schema_version = "1"
notes = "Test fixture: matches the synthetic Cargo.lock exactly."

[[declared_crates]]
name = "serde"
ecosystem = "cargo"
"#;
    write_trust_budget(tmp.path(), toml_body);

    let result = analyze_trust_budget(tmp.path().to_str().unwrap()).await;

    let drift_findings = findings_by_prefix(&result, "trust_budget:undeclared:");
    let over_findings = findings_by_prefix(&result, "trust_budget:overdeclared:");
    assert!(
        drift_findings.is_empty(),
        "expected zero undeclared findings; got: {:?}",
        drift_findings
    );
    assert!(
        over_findings.is_empty(),
        "expected zero overdeclared findings; got: {:?}",
        over_findings
    );

    // Score should be 100 — fully aligned.
    assert_eq!(result["score"], 100, "fully-aligned trust-budget should score 100");
}

