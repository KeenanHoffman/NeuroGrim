// ── 10. Recursion guard — Q7 hard rule ──────────────────────────────

/// The validator MUST be pure file-read + JSON-Schema-validate. NO
/// shell-out. NO `std::process::Command`. NO `Stdio`. NO `std::process`.
/// This test reads the source of `trust_budget.rs`, locates the
/// `analyze_trust_budget` and `analyze_trust_budget_path` function
/// spans, and grep-checks for forbidden patterns within the entire
/// file (since the trust-budget logic lives in helper functions called
/// from `analyze_trust_budget_path`, we apply the check to the whole
/// file — there's no other public entry point that could legally
/// shell out).
#[test]
fn recursion_guard_no_command_in_validator_span() {
    let path = locate_trust_budget_source();
    let source = std::fs::read_to_string(&path)
        .unwrap_or_else(|e| panic!("read {}: {e}", path.display()));

    // Validator's own surface (the file) — this includes
    // `analyze_trust_budget`, `analyze_trust_budget_path`, and all
    // their helper functions. The C3 design has all logic in this
    // single file (per the brief: "Single file (mirror
    // `capability_hygiene.rs` pattern)"); so the file-level scan IS
    // the validator-span scan.

    let forbidden = [
        "std::process::Command",
        "process::Command",
        "Command::new",
        "Stdio",
        "std::process",
        "duct::cmd",
        "subprocess::",
    ];
    for pat in forbidden.iter() {
        assert!(
            !source.contains(pat),
            "Q7 recursion-guard violated: `trust_budget.rs` contains forbidden \
             shell-execution pattern `{pat}`. The validator must be pure file-read + \
             JSON-Schema-validate. See E-B2-4 plan Q7."
        );
    }

    // Defense-in-depth: also reject patterns that suggest the
    // validator is donning a hat or invoking the Skill / Bash / Edit
    // / Write tool surface — same defensive posture as
    // `hat_contract_sensor_behavior.rs::recursion_guard_no_command_in_validator_span`.
    let suspicious = ["Skill::", "execute_skill", "invoke_tool"];
    for pat in suspicious.iter() {
        assert!(
            !source.contains(pat),
            "Q7 recursion-guard tripped on suspicious pattern `{pat}` inside \
             `trust_budget.rs`. The validator should not invoke tools or skills \
             — surface the violation to the operator instead."
        );
    }

    // Cargo.toml dependency check — assert no shell-execution crates
    // were added to neurogrim-sensory. The brief explicitly forbids
    // `walkdir`, `subprocess`, `duct`, `which`, `shell-words`.
    let cargo_toml_path = PathBuf::from(env!("CARGO_MANIFEST_DIR")).join("Cargo.toml");
    let cargo_toml = std::fs::read_to_string(&cargo_toml_path)
        .unwrap_or_else(|e| panic!("read {}: {e}", cargo_toml_path.display()));
    let forbidden_deps = [
        "\nwalkdir = ",
        "\nwhich = ",
        "\nsubprocess = ",
        "\nduct = ",
        "\nshell-words = ",
    ];
    for dep in forbidden_deps.iter() {
        assert!(
            !cargo_toml.contains(dep),
            "Q7 recursion-guard violated at dependency level: Cargo.toml grew \
             shell-execution / walker dependency `{}`. Trust-budget must not link \
             to shell-execution crates. See E-B2-4 plan Q7 + Q1.",
            dep.trim()
        );
    }
}
