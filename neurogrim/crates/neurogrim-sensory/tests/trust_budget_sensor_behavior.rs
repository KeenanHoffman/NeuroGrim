// ── 5. Seeded crate suppresses undeclared finding ───────────────────

#[tokio::test]
async fn seeded_crate_suppresses_undeclared_finding() {
    let tmp = make_brain_root();
    write_cargo_lock(tmp.path(), &["bootstrap_dep"]);
    // TOML declares it as seeded — should NOT fire undeclared.
    let toml_body = r#"schema_version = "1"

[[declared_crates]]
name = "bootstrap_dep"
ecosystem = "cargo"
seeded = true
"#;
    write_trust_budget(tmp.path(), toml_body);

    let result = analyze_trust_budget(tmp.path().to_str().unwrap()).await;

    // No undeclared OR overdeclared findings naming bootstrap_dep.
    let bootstrap_findings: Vec<&Value> = trust_budget_findings(&result)
        .into_iter()
        .filter(|f| {
            f["name"]
                .as_str()
                .map(|n| n.contains("bootstrap_dep"))
                .unwrap_or(false)
        })
        .collect();
    assert!(
        bootstrap_findings.is_empty(),
        "seeded + present-in-actual must produce ZERO findings about bootstrap_dep; got: {:?}",
        bootstrap_findings
    );
}

