// ── 12. Crate scanner: filtered to direct deps only ─────────────────

/// Bug-2 regression: synthetic two-level layout under `neurogrim/`
/// declaring exactly one workspace member (`crates/foo`) which depends
/// on `serde`. Synthetic `Cargo.lock` containing `serde` AND a fake
/// transitive `serde-deserialize-internal`. Sensor MUST emit
/// `:undeclared:crate:serde` but NOT
/// `:undeclared:crate:serde-deserialize-internal`.
#[tokio::test]
async fn crate_filter_to_direct_deps_only() {
    let tmp = make_brain_root();

    // NeuroGrim two-level layout: workspace at <root>/neurogrim/.
    let ws = tmp.path().join("neurogrim");
    std::fs::create_dir_all(&ws).expect("create ws dir");

    // Workspace Cargo.toml declaring `serde` in [workspace.dependencies]
    // and `crates/foo` as the sole member.
    std::fs::write(
        ws.join("Cargo.toml"),
        r#"[workspace]
resolver = "2"
members = ["crates/foo"]

[workspace.dependencies]
serde = "1"
"#,
    )
    .expect("write workspace Cargo.toml");

    // Member crate manifest with serde as a direct dep.
    let foo_dir = ws.join("crates").join("foo");
    std::fs::create_dir_all(&foo_dir).expect("create foo dir");
    std::fs::write(
        foo_dir.join("Cargo.toml"),
        r#"[package]
name = "foo"
version = "0.1.0"

[dependencies]
serde = { workspace = true }
"#,
    )
    .expect("write foo Cargo.toml");

    // Lockfile containing the direct dep AND a synthetic transitive.
    write_cargo_lock(
        ws.as_path(),
        &["serde", "serde-deserialize-internal"],
    );

    let result = analyze_trust_budget(tmp.path().to_str().unwrap()).await;

    let direct =
        findings_by_prefix(&result, "trust_budget:undeclared:crate:serde");
    // Exactly the direct dep, NOT the transitive variant. We use an
    // exact-name match here (the prefix `serde` is shared).
    let serde_exact: Vec<&serde_json::Value> = direct
        .into_iter()
        .filter(|f| {
            f["name"].as_str() == Some("trust_budget:undeclared:crate:serde")
        })
        .collect();
    assert_eq!(
        serde_exact.len(),
        1,
        "expected exactly 1 undeclared:crate:serde finding (direct dep); got: {:?}",
        trust_budget_findings(&result)
    );

    let transitive = findings_by_prefix(
        &result,
        "trust_budget:undeclared:crate:serde-deserialize-internal",
    );
    assert!(
        transitive.is_empty(),
        "transitive crate `serde-deserialize-internal` MUST NOT fire — direct-only filter (Bug 2 + Q1 lock); got: {:?}",
        transitive
    );
}

