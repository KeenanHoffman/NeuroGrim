// ── Public types ──────────────────────────────────────────────────────

/// Single message on the bus. Mirrors the schema sketched in the S13
/// epic (`epics/S13-agent-coordination-bus.md` §S13-B-1).
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
pub struct QueueMessage {
    /// UUID v4. Generated by [`QueueMessage::new`]. Stable across
    /// retries — operators replaying a message keep the same id.
    pub id: Uuid,
    /// Topic name. See [`Topic`] for namespace rules.
    pub topic: String,
    /// Free-form payload. The bus is payload-agnostic; consumers
    /// negotiate shape per-topic (typed agents declare schemas in
    /// `queue-config.yaml`; raw consumers handle untyped JSON).
    pub payload: serde_json::Value,
    /// RFC3339 UTC timestamp set by [`QueueMessage::new`].
    pub produced_at: DateTime<Utc>,
    /// Coarse priority classification. Readers may choose to honor or
    /// ignore. Default: [`Priority::Normal`].
    pub priority: Priority,
    /// Optional expiry. Readers SHOULD filter out expired messages.
    /// `None` = never expires.
    #[serde(skip_serializing_if = "Option::is_none", default)]
    pub expires_at: Option<DateTime<Utc>>,
}

/// Coarse priority enum. Three buckets — finer gradations would be
/// over-engineering for a v1 bus.
#[derive(Debug, Clone, Copy, Serialize, Deserialize, PartialEq, Eq, Hash)]
#[serde(rename_all = "lowercase")]
pub enum Priority {
    Low,
    Normal,
    High,
}

impl Default for Priority {
    fn default() -> Self {
        Priority::Normal
    }
}

impl QueueMessage {
    /// Build a new message with a fresh UUID v4 + current UTC
    /// timestamp.
    pub fn new(topic: impl Into<String>, payload: serde_json::Value) -> Self {
        Self {
            id: Uuid::new_v4(),
            topic: topic.into(),
            payload,
            produced_at: Utc::now(),
            priority: Priority::Normal,
            expires_at: None,
        }
    }

    /// Builder-style priority setter.
    pub fn with_priority(mut self, priority: Priority) -> Self {
        self.priority = priority;
        self
    }

    /// Builder-style expiry setter.
    pub fn with_expires_at(mut self, expires_at: DateTime<Utc>) -> Self {
        self.expires_at = Some(expires_at);
        self
    }

    /// True iff the message is past its `expires_at`. `None` →
    /// never expired.
    pub fn is_expired(&self, now: DateTime<Utc>) -> bool {
        match self.expires_at {
            Some(exp) => now >= exp,
            None => false,
        }
    }

    /// Approximate serialized line size in bytes. Callers writing
    /// directly to JSONL files use this to self-police the
    /// PIPE_BUF (~4 KB) atomicity boundary — messages above the
    /// boundary lose concurrent-write atomicity guarantees.
    pub fn serialized_size(&self) -> usize {
        // +1 for the trailing newline that `append` adds.
        serde_json::to_string(self).map(|s| s.len() + 1).unwrap_or(0)
    }
}

// ── Topic namespace helpers ───────────────────────────────────────────

/// Stateless helpers for topic-name validation. The bus doesn't
/// own a registry of topics (creation is implicit on first
/// publish); these helpers exist so writers + the doctor check can
/// agree on shape.
pub struct Topic;

impl Topic {
    /// True iff the topic is in the reserved system-namespace.
    /// Reserved names are `_neurogrim/<lowercase-kebab>`.
    pub fn is_reserved(name: &str) -> bool {
        name.starts_with(RESERVED_PREFIX)
    }

    /// True iff the topic is a syntactically-valid adopter-defined
    /// name. Rules:
    /// - NOT in the reserved namespace
    /// - lowercase ASCII alphanumeric + `-` + `/`
    /// - at least one character before the first `/`
    /// - no leading or trailing `/` or `-`
    /// - no consecutive separators (`//`, `--`)
    ///
    /// `neurogrim doctor` enforces this against `queue-config.yaml`
    /// at validation time.
    pub fn is_valid_adopter_topic(name: &str) -> bool {
        if Self::is_reserved(name) {
            return false;
        }
        if name.is_empty() {
            return false;
        }
        let bytes = name.as_bytes();
        if bytes.first() == Some(&b'/')
            || bytes.last() == Some(&b'/')
            || bytes.first() == Some(&b'-')
            || bytes.last() == Some(&b'-')
        {
            return false;
        }
        // Disallow consecutive separators. Allowed character class:
        // ASCII lowercase letters, digits, `-`, `/`.
        let mut prev = b' ';
        for &b in bytes {
            let ok = matches!(b, b'a'..=b'z' | b'0'..=b'9' | b'-' | b'/');
            if !ok {
                return false;
            }
            if (b == b'/' || b == b'-') && (prev == b'/' || prev == b'-') {
                return false;
            }
            prev = b;
        }
        true
    }

    /// True iff the topic is either a reserved system topic OR a
    /// valid adopter topic. Useful for code paths that legitimately
    /// publish either (e.g., the dashboard's bus service).
    pub fn is_valid(name: &str) -> bool {
        Self::is_reserved(name) || Self::is_valid_adopter_topic(name)
    }
}

// ── Append-only writer ────────────────────────────────────────────────

/// Append a single message as one JSONL line. Atomic for
/// `serialized_size() < PIPE_BUF` (4 KB on POSIX); concurrent
/// writers within that bound never interleave.
///
/// Callers needing `produced_at` to be set by the bus rather than
/// the caller should use [`QueueMessage::new`] before calling.
pub fn append(path: &Path, msg: &QueueMessage) -> Result<()> {
    if !Topic::is_valid(&msg.topic) {
        bail!(
            "queue: invalid topic name {:?} — adopters use lowercase \
             kebab-case `<scope>/<name>`; system topics use `_neurogrim/<name>`",
            msg.topic
        );
    }
    if let Some(parent) = path.parent() {
        std::fs::create_dir_all(parent)
            .context("queue: create parent dir for ledger")?;
    }
    let line = serde_json::to_string(msg).context("queue: serialize message")?;
    if line.contains('\n') {
        bail!("queue: serialized message contained a newline (multi-line JSON?)");
    }
    let mut f = OpenOptions::new()
        .create(true)
        .append(true)
        .open(path)
        .with_context(|| format!("queue: open {} for append", path.display()))?;
    writeln!(f, "{line}").context("queue: write line")?;
    f.flush().context("queue: flush")?;
    Ok(())
}

// ── Reader ────────────────────────────────────────────────────────────

/// Read messages from a JSONL queue file, starting at the Nth line
/// (zero-indexed; `since_offset` is line-number, not byte-offset).
///
/// Behavior:
/// - Missing file → empty iterator (the queue hasn't received any
///   messages yet; consumer offset 0 is well-defined).
/// - I/O failure on read → error returned from
///   [`JsonlQueueReader::open`]; iteration is infallible.
/// - Malformed line → logged at `tracing::warn!` + skipped (matches
///   the calibration-ledger reader's posture).
/// - Expired messages: NOT filtered by the reader; consumers who
///   care call [`QueueMessage::is_expired`].
pub struct JsonlQueueReader {
    /// One entry per non-empty line in the file at construction
    /// time. Snapshot semantics — concurrent appends after `open`
    /// won't appear in this iterator. Re-open to drain new entries.
    messages: Vec<QueueMessage>,
}

impl JsonlQueueReader {
    /// Open + parse all lines. Returns an iterator-shaped wrapper
    /// you can call `iter_from(offset)` on.
    pub fn open(path: &Path) -> Result<Self> {
        let raw = match std::fs::read_to_string(path) {
            Ok(s) => s,
            Err(e) if e.kind() == std::io::ErrorKind::NotFound => {
                return Ok(Self { messages: Vec::new() });
            }
            Err(e) => {
                return Err(e).with_context(|| {
                    format!("queue: read {} for iteration", path.display())
                });
            }
        };
        let mut messages = Vec::new();
        for (idx, line) in raw.lines().enumerate() {
            if line.trim().is_empty() {
                continue;
            }
            match serde_json::from_str::<QueueMessage>(line) {
                Ok(m) => messages.push(m),
                Err(e) => {
                    tracing::warn!(
                        "queue: malformed JSONL line {} in {}: {} — skipping",
                        idx + 1,
                        path.display(),
                        e
                    );
                }
            }
        }
        Ok(Self { messages })
    }

    /// Total parseable messages snapshotted at `open` time.
    pub fn len(&self) -> usize {
        self.messages.len()
    }

    pub fn is_empty(&self) -> bool {
        self.messages.is_empty()
    }

    /// All messages whose offset (line index, 0-based) is `>= since`.
    /// Useful for the consumer-resume pattern: persist last-seen
    /// offset, call `iter_from(last + 1)` next time.
    pub fn iter_from(&self, since: usize) -> impl Iterator<Item = &QueueMessage> {
        let start = since.min(self.messages.len());
        self.messages[start..].iter()
    }

    /// Consume the reader and return the underlying snapshot. Useful
    /// for callers wanting to take ownership of the entries.
    pub fn into_messages(self) -> Vec<QueueMessage> {
        self.messages
    }

    /// All messages NOT past their expiry as of `now`.
    pub fn live_messages(&self, now: DateTime<Utc>) -> impl Iterator<Item = &QueueMessage> {
        self.messages.iter().filter(move |m| !m.is_expired(now))
    }

    /// Most recent N messages (oldest first within the slice).
    /// Equivalent to `tail -n` on the underlying file but safe across
    /// malformed-line skips.
    pub fn tail(&self, count: usize) -> &[QueueMessage] {
        let start = self.messages.len().saturating_sub(count);
        &self.messages[start..]
    }
}

// ── Retention / compaction (S13-B-7 expansion) ───────────────────────

/// Per-topic retention policy. Default values match the v4.1 epic
/// (refinement #7): 30 days OR 10k messages, whichever fires first.
/// Either field set to None disables that limit.
#[derive(Debug, Clone, Copy)]
pub struct RetentionPolicy {
    pub max_days: Option<u32>,
    pub max_messages: Option<u32>,
}

impl Default for RetentionPolicy {
    fn default() -> Self {
        Self {
            max_days: Some(30),
            max_messages: Some(10_000),
        }
    }
}

/// Result of a `compact` invocation. Surfaces what got moved/dropped
/// so the CLI can print a useful summary.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct CompactReport {
    pub topic_path: PathBuf,
    pub archive_path: PathBuf,
    pub kept: usize,
    pub archived: usize,
    pub dropped_expired: usize,
}

/// Compact a topic file in place: rotate older entries beyond the
/// retention policy to `<topic>.archive.jsonl` next to the live file,
/// drop expired messages entirely (they can't be useful to consumers
/// past their TTL), atomic-write the trimmed live file.
///
/// Atomicity: writes the trimmed file via a temp + rename so a
/// concurrent reader either sees the old full file or the new
/// trimmed file — never a torn write.
///
/// Returns a [`CompactReport`] with counts for the operator-facing
/// summary.
pub fn compact(path: &Path, policy: RetentionPolicy) -> Result<CompactReport> {
    let archive_path = {
        // <name>.jsonl → <name>.archive.jsonl (next to the live file)
        let mut p = PathBuf::from(path);
        let stem = p
            .file_stem()
            .map(|s| s.to_string_lossy().into_owned())
            .unwrap_or_else(|| "queue".to_string());
        p.set_file_name(format!("{stem}.archive.jsonl"));
        p
    };

    let report_skeleton = CompactReport {
        topic_path: path.to_path_buf(),
        archive_path: archive_path.clone(),
        kept: 0,
        archived: 0,
        dropped_expired: 0,
    };

    let raw = match std::fs::read_to_string(path) {
        Ok(s) => s,
        Err(e) if e.kind() == std::io::ErrorKind::NotFound => return Ok(report_skeleton),
        Err(e) => {
            return Err(anyhow::Error::from(e)
                .context(format!("queue: read {} for compact", path.display())));
        }
    };

    // Parse all entries (line-by-line). Carry the original line text
    // alongside the parsed message so we don't re-serialize on
    // archive-write — that would mutate field ordering / formatting
    // gratuitously.
    let mut entries: Vec<(String, QueueMessage)> = Vec::new();
    let mut malformed: Vec<String> = Vec::new();
    for (idx, line) in raw.lines().enumerate() {
        if line.trim().is_empty() {
            continue;
        }
        match serde_json::from_str::<QueueMessage>(line) {
            Ok(m) => entries.push((line.to_string(), m)),
            Err(e) => {
                tracing::warn!(
                    "queue compact: malformed line {} skipped: {}",
                    idx + 1,
                    e
                );
                malformed.push(line.to_string());
            }
        }
    }

    let now = Utc::now();

    // Step 1: drop expired.
    let pre_count = entries.len();
    entries.retain(|(_, m)| !m.is_expired(now));
    let dropped_expired = pre_count - entries.len();

    // Step 2: max_days windowing.
    if let Some(days) = policy.max_days {
        let cutoff = now - chrono::Duration::days(days as i64);
        let (old, fresh): (Vec<_>, Vec<_>) =
            entries.into_iter().partition(|(_, m)| m.produced_at < cutoff);
        // Archive the old.
        if !old.is_empty() {
            append_lines_to_archive(&archive_path, old.iter().map(|(l, _)| l.as_str()))?;
        }
        let archived_now = old.len();
        entries = fresh;
        // Step 3: max_messages cap.
        let archived_more = if let Some(max) = policy.max_messages {
            cap_to_max(&mut entries, max as usize, &archive_path)?
        } else {
            0
        };
        let archived = archived_now + archived_more;
        write_trimmed(path, &entries, &malformed)?;
        return Ok(CompactReport {
            topic_path: path.to_path_buf(),
            archive_path,
            kept: entries.len(),
            archived,
            dropped_expired,
        });
    }

    // Only max_messages.
    let archived = if let Some(max) = policy.max_messages {
        cap_to_max(&mut entries, max as usize, &archive_path)?
    } else {
        0
    };
    write_trimmed(path, &entries, &malformed)?;
    Ok(CompactReport {
        topic_path: path.to_path_buf(),
        archive_path,
        kept: entries.len(),
        archived,
        dropped_expired,
    })
}

/// If `entries.len() > max`, move the oldest excess to the archive.
/// Returns the count moved.
fn cap_to_max(
    entries: &mut Vec<(String, QueueMessage)>,
    max: usize,
    archive_path: &Path,
) -> Result<usize> {
    if entries.len() <= max {
        return Ok(0);
    }
    let drop_count = entries.len() - max;
    let to_archive: Vec<_> = entries.drain(..drop_count).collect();
    append_lines_to_archive(archive_path, to_archive.iter().map(|(l, _)| l.as_str()))?;
    Ok(drop_count)
}

fn append_lines_to_archive<'a, I>(archive_path: &Path, lines: I) -> Result<()>
where
    I: IntoIterator<Item = &'a str>,
{
    if let Some(parent) = archive_path.parent() {
        std::fs::create_dir_all(parent).context("queue compact: create archive parent")?;
    }
    let mut f = OpenOptions::new()
        .create(true)
        .append(true)
        .open(archive_path)
        .with_context(|| format!("queue compact: open archive {}", archive_path.display()))?;
    for line in lines {
        writeln!(f, "{line}").context("queue compact: write archive line")?;
    }
    f.flush().context("queue compact: flush archive")?;
    Ok(())
}

/// Atomically rewrite the live topic file with `kept` entries plus
/// any malformed lines we preserve in place (best-effort — we'd
/// rather keep operator-curated debug data than silently drop it).
fn write_trimmed(
    path: &Path,
    kept: &[(String, QueueMessage)],
    preserved_malformed: &[String],
) -> Result<()> {
    let mut tmp_path = path.to_path_buf();
    let mut tmp_name = path
        .file_name()
        .map(|n| n.to_string_lossy().into_owned())
        .unwrap_or_else(|| "queue.jsonl".to_string());
    tmp_name.push_str(".tmp");
    tmp_path.set_file_name(tmp_name);

    {
        let mut f = OpenOptions::new()
            .create(true)
            .write(true)
            .truncate(true)
            .open(&tmp_path)
            .with_context(|| format!("queue compact: open temp {}", tmp_path.display()))?;
        for line in preserved_malformed {
            writeln!(f, "{line}").context("queue compact: write malformed-preserve")?;
        }
        for (line, _) in kept {
            writeln!(f, "{line}").context("queue compact: write kept")?;
        }
        f.flush().context("queue compact: flush temp")?;
    }

    std::fs::rename(&tmp_path, path).with_context(|| {
        format!("queue compact: rename {} → {}", tmp_path.display(), path.display())
    })?;
    Ok(())
}

