//! Append-only event-log queue substrate (S13-B-1, v4.1).
//!
//! This is **Pattern 1** from S13's two-patterns-one-primitive split:
//! a fan-out, multi-consumer event log with per-reader offsets and no
//! acknowledgement. Pattern 2 (request/response coordination —
//! `await_approval`) builds on this primitive but ships its API
//! distinctly in S13-B-5.
//!
//! ## Invariants
//!
//! - **Append-only**: writers never seek; concurrent writers are safe
//!   for `< PIPE_BUF` (4 KB) lines via POSIX `O_APPEND` atomicity.
//!   `QueueMessage::serialized_size()` lets callers self-police.
//! - **Forward-only iteration**: `JsonlQueueReader` exposes a
//!   `since_offset` cursor; callers persist their own offset (the log
//!   never tracks who's read what).
//! - **Pure I/O**: this module ships in `neurogrim-core` because the
//!   shape is universal; the dashboard, MCP server, and CLI all wrap
//!   it differently. No HTTP, no async, no tokio.
//! - **Malformed-line tolerance**: a corrupt line never wedges a
//!   reader; iteration logs `tracing::warn!` and skips. Same
//!   discipline as `calibration_ledger::JsonlIter` (the prior art
//!   this module inherits from).
//!
//! ## Reserved namespace
//!
//! Topic IDs of the form `_neurogrim/<name>` are RESERVED for system
//! topics (e.g., `_neurogrim/approvals`, `_neurogrim/notifications`).
//! Adopters MUST NOT publish into them. [`Topic::is_reserved`] +
//! [`Topic::is_valid_adopter_topic`] are the syntactic checks; the
//! authoritative enforcement is in `neurogrim doctor` (S13-B-7).
//!
//! ## Schema
//!
//! ```json
//! {
//!   "id":          "<uuid v4>",
//!   "topic":       "<scope>/<name>",
//!   "payload":     {…arbitrary JSON…},
//!   "produced_at": "2026-04-29T13:42:18Z",
//!   "priority":    "low" | "normal" | "high",
//!   "expires_at":  "2026-04-30T13:42:18Z"      // optional
//! }
//! ```

