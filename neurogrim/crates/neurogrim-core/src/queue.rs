    fn adopter_topic_validation_rejects_reserved_and_malformed() {
        // Reserved namespace
        assert!(!Topic::is_valid_adopter_topic("_neurogrim/approvals"));
        // Empty / leading / trailing separators
        assert!(!Topic::is_valid_adopter_topic(""));
        assert!(!Topic::is_valid_adopter_topic("/leading"));
        assert!(!Topic::is_valid_adopter_topic("trailing/"));
        assert!(!Topic::is_valid_adopter_topic("-leading-dash"));
        assert!(!Topic::is_valid_adopter_topic("trailing-dash-"));
        // Consecutive separators
        assert!(!Topic::is_valid_adopter_topic("scope//name"));
        assert!(!Topic::is_valid_adopter_topic("scope--name"));
        // Disallowed characters
        assert!(!Topic::is_valid_adopter_topic("Scope/name")); // uppercase
        assert!(!Topic::is_valid_adopter_topic("scope/Name")); // uppercase
        assert!(!Topic::is_valid_adopter_topic("scope:name")); // `:` reserved per spec
        assert!(!Topic::is_valid_adopter_topic("scope.name")); // `.` not allowed
        assert!(!Topic::is_valid_adopter_topic("scope name")); // space
    }

